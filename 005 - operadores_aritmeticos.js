// Adição
let v1 = 10;
let v2 = 15;
let soma = v1+v2;
console.log(v1+v2)
console.log("A soma é: "+soma)
console.log("A soma é:",v1+v2)
console.log("A soma é: "+parseInt(v1+v2))
console.log("A soma é: "+Number(v1+v2))

// Subtração
let v3 = 10;
